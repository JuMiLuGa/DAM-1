
public class Quintaclase {

	public static void main(String[] args) {
		double alto, ancho;
		alto=4;
		ancho=2.5;
		
		System.out.println("El area del rectangulo de "+ alto +"cm de alto y "+ ancho +"cm de ancho es de: "+ (alto*ancho)+ "cm");
		System.out.println("El perimetro del rectangulo de "+ alto +"cm de alto y "+ ancho +"cm de ancho es de: "+ ((alto+ancho)*2)+ "cm");

	}

}
